								Syeh Arifin menurut cerita dan penuturan sesepuh / tetua Desa Mertapada berasal dari Baghdad (Irak) yang sengaja datang ke Cirebon guna memperdalam dan menyebarkan ajaran Islam ditanah Cirebon. Dalam perjalanan Syiarnya beliau kemudian menemukan suatu tempat sebelah timur kota Cirebon yang masih merupakan kampung yang subur dan aman . Syeh Arifin tertarik dengan tempat itu dan akhirnya beliau berniat untuk menjadikan tempat itu sebagai tempat tinggalnya, selanjutnya kampung itu  diberi nama kampung Mertalaya, Merta artinya tempat dan laya artinya senang, aman dan makmur. Sehingga Mertalaya mengandung arti suatu tempat yang aman, makmur dan menyenangkan.<br><br>
								Setelah Syeh Arifin menetap di Mertalaya, semenjak itu banyak orang-orang berdatangan berguru dan menimba ilmu dan menjadi santri kepadanya. Diantara sekian banyak santri yang ada diperguruan Mertalaya terdapat tiga pemuda yang berasal dari Negri Cempa (Kamboja) yang menjadi muridnya. Ketiga pemuda tersebut mempunyai kepandaian  dan kesaktian yang luar biasa, mereka itu adalah Selarasa, Selaganda dan Selasuara. Selama berada di Mertalaya ketiga pemuda tersebut mendapat bimbingan dan pendidikan Islam dari Syeh Arifin, disamping itu mendapatkan pelajaran-pelajaran lain yang berupa kesaktian yang biasa dikenal dengan istilah kekebalan atau kedigjayaan (Kanuragan), sehingga ketiganya terkenal dengan kepandaian dan kesaktian yang tak terkalahkan pada saat itu.<br><br>
								   Oleh karenanya suatu hari Syeh Arifin menagadakan uji tanding terhadap ketiga santrinya itu untuk menentukan siapa yang terkuat dan terpandai dari ketiganya.
								Namun setelah diadakan pertandingan ternyata tidak ada yang kalah dan tidak ada yang menang oleh karena ketiganya sama pandai dan sama sakti, maka Syeh Arifin mengadakan musyawarah dengan santri-santrinya yang lain, dan hasil musyawaran itu adalah merubah nama kampung Mertalaya menjadi Mertapada. Merta artinya tempat (panggonan) Pada artinya sama (sama pandai dan sakti), sehingga Mertapada mengandung pengertian suatu tempat yang dihuni oleh orang-orang yang berkepandaian dan kesaktian sama. Dan setelah itu dibentuk pula Ketua Kampung ( Kuwu ) sebagai orang yang mengurus masyarakat atau penduduk dan akhirnya Ketua Kampung diserahkan kepada Selarasa. Setelah beberapa tahun lamanya akhirnya Syeh Arifin wafat (Tanpa tahun) dan di kebumikan di Mertapada, dan oleh Masyarakat atau penduduk Mertapada tempat dikebumikannya Syeh Arifin dijadikan sebagai tempat Keramat Kibuyutan dengan<br><br>
								Sebutan Kibuyut Serpin (asal kata dari Arifin/Syarifin/Sarpin/Serpin). Setelah Syeh Arifin wafat, beberapa tahun kemudian ketiga santrinyapun wafat dan dikebumikan disamping kuburan Syeh Arifin.
								Sepeninggal Syeh Arifin dan ketiga santrinya beberapa tahun kemudian kekuasaan mertapada dipegang oleh Kidemang Ampunantara sampai wafatnya, selanjutnya digantikan oleh Nyi Mursifah Istri kidemang Ampunantara. Pada saat Nyi Mursipah memimpin Mertapada, beliau membagi Mertapada menjadi dua bagian, yaitu Mertapada bagian Timur disebut Mertapadawetan dan diberikan kepada anak laki-laki (anak sulungnya), sedangkan Mertapada bagian Barat disebut Mertapadakulon yang diberikan kepada anak perempuan dan menantunya.. Semenjak itulah Mertapada terbagi menjadi dua kampung atau desa yang dikenal dengan nama Desa Mertapadawetan dan Desa Mertapadakulon.
								 Beberapa Ratus Tahun kemudian setelah mengalami pergantian-pergantian pemimpin, akhirnya tercatat pada tahun 1844 -1863 Desa Mertapadawetan dipimpin oleh Yai Rasijem .</p>
